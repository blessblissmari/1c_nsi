"""Тесты правил нормализации моделей (ТЗ §8.3)."""

from __future__ import annotations

from app.services.normalization import normalize_model_name


def _t(inp: str, expected: str) -> None:
    out = normalize_model_name(inp)
    assert out == expected, f"{inp!r} -> {out!r} (ожидалось {expected!r})"


def test_rules():
    # 1) Верхний регистр + Ё->Е
    _t("насос ёжик", "НАСОС-ЕЖИК")

    # 2) Запятая между цифрами -> точка
    _t("АВС 12,5 3,2", "АВС12.5-3.2")

    # 3/4/7) Между цифрой/буквой все разделители удаляются
    _t("НАСОС-12 / А", "НАСОС12А")

    # 5/8) Между цифрами неточка -> дефис; пробел -> дефис
    _t("МОД 12 34", "МОД12-34")
    _t("МОД 12/34", "МОД12-34")

    # 6/8) Между буквами любой разделитель/пробел -> дефис
    _t("ЭЛЕКТРО ДВИГАТЕЛЬ_АСИНХР", "ЭЛЕКТРО-ДВИГАТЕЛЬ-АСИНХР")

    # 10) № + цифры в скобки в конце
    _t("УКДТ654-02П-№2,5", "УКДТ654-02П(№2.5)")

    # 8.3.10 — латиница-омоглиф -> кириллица
    _t("ПP-12", "ПР12")
